def rebuild_pdf_data_js() -> None:
    files = sorted(p.name for p in PDF_DIR.glob("*.pdf"))
    payload = {}
    for name in files:
        payload[name] = base64.b64encode((PDF_DIR / name).read_bytes()).decode("ascii")
    lines = ["(() => {", "  window.DEMO_PDF_BASE64 = {"]
    for i, name in enumerate(files):
        comma = "," if i < len(files) - 1 else ""
        lines.append(f'    {json.dumps(name)}: {json.dumps(payload[name])}{comma}')
    lines.append("  };")
    lines.append("})();")
    lines.append("")
    APP_PDF_DATA.write_text("\n".join(lines), encoding="utf-8")
    print(f"wrote {APP_PDF_DATA} ({APP_PDF_DATA.stat().st_size} bytes, {len(files)} files)")


