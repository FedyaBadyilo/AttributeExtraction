def toc_page(s, items: list[tuple[str, str]]):
    story = [P("Содержание", s["h1"])]
    for title, page in items:
        dots = "." * max(4, 48 - len(title))
        story.append(P(f"{title} {dots} {page}", s["toc"]))
    story.append(PageBreak())
    return story


