def write_pdf(path: Path, story, *, title: str, pagesize=A4, margins=None) -> None:
    margins = margins or dict(left=18 * mm, right=18 * mm, top=16 * mm, bottom=16 * mm)
    doc = SimpleDocTemplate(
        str(path),
        pagesize=pagesize,
        title=title,
        author="demo-build_demo_pdfs",
        **margins,
    )
    doc.build(story)
    print(f"wrote {path.name} ({path.stat().st_size} bytes)")


